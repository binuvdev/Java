package com.java.study;

public class BitStudy {
	
	
	public static void main (String[] args) {
		
		int x = 7;
		
		System.out.println(Integer.toBinaryString(x));
		System.out.println(Integer.toBinaryString(x << 1));
		System.out.println(Integer.toBinaryString(x >> 1));
		//System.out.println(Integer.toBinaryString(x <<< 1));
		
	}

}
